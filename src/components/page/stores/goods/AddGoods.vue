<script>
import axios from "axios";
import { createNamespacedHelpers } from "vuex";
const { mapActions, mapMutations, mapState } = createNamespacedHelpers(
  "storeModule" //模块名
);
export default {
  data() {
    return {
      dialogFormVisible: false,
      form: {
        name: "",
        title: "",
        type: "",
        method: "",
        applySfc: "",
        exclusiveSfc: "",
        total: "",
        packSfc: "",
        flavor: "",
        specialFuc: "",
        placeOfOrigin: "",
        date: "",
        shelfLife: "",
        features: "",
        price: "",
        newPrice: "",
        sales: 0,
        images: [],
        goodState: 1
      },
      formLabelWidth: "120px",
      dialogImageUrl: "",
      dialogVisible: false,
      rules: {
        name: [
          { required: true, message: "请输入活动名称", trigger: "blur" },
          { min: 3, max: 5, message: "长度在 3 到 5 个字符", trigger: "blur" }
        ],
        region: [
          { required: true, message: "请选择活动区域", trigger: "change" }
        ],
        date1: [
          {
            type: "date",
            required: true,
            message: "请选择日期",
            trigger: "change"
          }
        ],
        date2: [
          {
            type: "date",
            required: true,
            message: "请选择时间",
            trigger: "change"
          }
        ],
        type: [
          {
            type: "array",
            required: true,
            message: "请至少选择一个活动性质",
            trigger: "change"
          }
        ],
        resource: [
          { required: true, message: "请选择活动资源", trigger: "change" }
        ],
        desc: [{ required: true, message: "请填写活动形式", trigger: "blur" }]
      }
    };
  },
  created() {
    console.log(this, 78);
  },
  computed: {
    ...mapState(["storeAddVisible", "storeId"]),
    addStoreDiolog: {
      get() {
        console.log(this.storeAddVisible);
        return this.storeAddVisible;
        //非map情况下，模块有嵌套的话，想要找到最里层的state 需要加上组件名(就是modules里面的属性名)，然后直接点属性
      },
      set(storeAddVisible) {
        console.log("updateSwitch", storeAddVisible);
        this.setStoreAddVisible(storeAddVisible);
        //非map情况下，模块有嵌套的话，想要提交。用模块名/属性名
      }
    }
  },
  methods: {
    ...mapMutations(["setStoreAddVisible"]),
    ...mapActions(["setStoregoods"]),
    // 确认添加
    addStoreGood(formName) {
      this.$refs[formName].validate(valid => {
        if (valid) {
          this.addStoreDiolog = false;
          console.log(this.$refs, "uuu");
          axios({
            url: "/storegoods",
            method: "post",
            data: {
              ...this.form,
              images: JSON.stringify(this.form.images),
              storeId: this.storeId
            }
          }).then(({ data }) => {
            this.setStoregoods();
            this.$refs[formName].resetFields();
          });
        } else {
          console.log("error submit!!");
          return false;
        }
      });
      console.log({ ...this.form, images: JSON.stringify(this.form.images) });
      // this.form.images = JSON.stringify(this.form.images);
    },
    handleAvatarSuccess(res, file) {
      this.form.images = [
        ...this.form.images,
        { name: file.name, url: "/upload/" + res }
      ];
      console.log(res, file);
    },
    handleRemove(file, fileList) {
      console.log(file, fileList);
      let arr = this.form.images;
      for (let i in arr) {
        if (arr[i].uid == file.uid) {
          arr.splice(i, 1);
          break;
        }
      }
      this.form.images = arr;
    },
    handlePreview(file) {
      console.log(file);
    },
    handlePictureCardPreview(file) {
      this.dialogImageUrl = file.url;
      this.dialogVisible = true;
    }
  }
};
</script>
